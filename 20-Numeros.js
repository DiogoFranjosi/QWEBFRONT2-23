// Utilizando o loop while mostre os número pares de 1 a 20
console.log("-------------------Pares-------------------");

let numero = 0;

while (numero <=20)
{
    console.log(numero);
    numero = numero + 2;
}
// Utilizando o loop for mostre os número ímpares de 1 a 20
console.log("-------------------Ímpares-------------------");

for (let i = 1; i <=20; i = i + 2)
{
    console.log(i);
}